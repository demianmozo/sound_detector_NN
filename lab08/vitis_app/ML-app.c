	XInference inferenceInstancePtr;
	XInference_Config *inferenceCfgPtr;

	init_platform();

	print("\n\r--- ML-based inference test --- \n\r");

	// HLS IP core configuration and initialization
	inferenceCfgPtr = XInference_LookupConfig(XPAR_INFERENCE_0_DEVICE_ID);
	if(!inferenceCfgPtr){
		print("\n\rLookup of ML inference IP core failed \n\r");
		return XST_FAILURE;
	}

	XStatus =  XInference_CfgInitialize(&inferenceInstancePtr, inferenceCfgPtr);
